-- ============================================================
-- Задание 4 — Производительность сотрудников           [COUNT + CONCAT]
-- ------------------------------------------------------------
-- Бизнес-вопрос: HR хочет оценить обработку заказов сотрудниками для
-- квартального бонуса.
-- Метрика: total_orders = COUNT(OrderID) по сотруднику.
-- Подмена (закон Гудхарта): как только COUNT(заказов) станет бонусом,
-- метрика перестаёт быть честной — заказы дробят, берут лёгкие, жертвуют
-- качеством. Честнее: выручка/маржа на сотрудника + сигнал качества,
-- которые труднее накрутить в одиночку.
-- Навыки: JOIN · GROUP BY · COUNT · CONCAT
-- ============================================================
SELECT
    CONCAT(e.FirstName, ' ', e.LastName) AS employee,
    COUNT(o.OrderID)                     AS total_orders
FROM Employees e
JOIN Orders o ON o.EmployeeID = e.EmployeeID
GROUP BY e.EmployeeID, e.FirstName, e.LastName
ORDER BY total_orders DESC;
